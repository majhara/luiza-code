# Lambda
## São funções normais, porém anônimas e que estão contidas em apenas uma linha de código
## Sintaxe: lambda argument(s): expression
## Você dá à função um valor(argumento) e então fornece a operação (expresão)

## Prós:
### Bom para operações lógicas simples que são fáceis de entender, isso torna o código mais legível também
### Bom para funções que serão usadas apenas uma vez

## Contras:
### Elas só podem executar uma única expressão, por isso têm que ser usadas somente no simples
### Se a função abrange mais de uma linha, esquece, não funcionará
### Dependendo do contexto ela vai ser executada somente uma vez