## Aritméticos: adição, subtração, multiplicação, divisão, divisão interna (realiza a divisão entre operandos e a parte decimal de ambos os operados), módulo, exponenciação
print("OPERADORES ARITMÉTICOS")
primeiro_valor = 4
segundo_valor = 2

soma = primeiro_valor + segundo_valor
print('soma = ', soma)

subtracao = primeiro_valor - segundo_valor
print('subtração = ', subtracao)

multiplicacao = primeiro_valor * segundo_valor
print('multiplicação = ', multiplicacao)

divisao = primeiro_valor / segundo_valor
print('divisão = ', divisao)

divisao_interna = primeiro_valor // segundo_valor
print('divisao interna = ', divisao_interna)

modulo = primeiro_valor % segundo_valor
print('modulo = ', modulo)

exponenciacao = primeiro_valor ** segundo_valor
print('exponenciacao = ', exponenciacao)

