## Comparativos: igual a, diferente de, maior que, maior ou igual, menor que, menor ou igual
print()
print("OPERADORES DE COMPARAÇÃO")
variavel = 5

if variavel == 5:
    print("Os valores são iguais")
    
if variavel != 7:
    print("O valor da variável não é igual a 7")

if variavel > 2:
    print("O valor da variável é maior que 2")
    
if variavel >= 5:
    print("O valor da variável é maior ou igual a 5")

if variavel > 7:
    print("O valor da variável é menor que 7")
    
if variavel <= 5:
    print("O valor da variável é menor ou igual a 5")
    