# # Programa Python no arquivo ex11.py: Este programa irá calcular a área de um
# quadrado. Peça para a pessoa informar a medida numérica de um lado do quadrado. E
# depois informe-lhe o valor da área do quadrado.

lado_quadrado = float(input("Digite o valor do lado do quadrado em cm: "))
area_quadrado = lado_quadrado * lado_quadrado
print(f"A área do quadrado é {area_quadrado} cm²")