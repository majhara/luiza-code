# POO
## É um paradigma de programação que tenta emular problemas do mundo real usando o conceito abstrato de objetos como uma estrutura de dados que pode ser alterada e referenciada
## Em suma, é um modelo que organiza o software em dados ao invés de lógica e funções

## Vantagens: Redução de custos, Economia de tempo e esforço, facilidade de alterações/manutenção

## Objetos: são a base da POO e pode ser qualquer coisa que represente coisas da vida real, é uma entidade que encapsula dados e comportamento, onde comportamento é o conjuntto de funções ou métodos que operam nesses dados

## Em Python, todo valor é um objeto
## Atributos são sempre adjetivos, enquanto os comportamentos (métodos) são sempre os verbos

## Pilares: Encapsulamento, abstração, herança, polimorfismo

# Abstração: processo de ocultar os detalhes de implementação do usuário, apenas a funcionalidade será fornecida. Em outras palavras, o usuário terá a informação sobre o que o objeto faz, ao invés de como ele faz

# Herança: Processo onde uma classe adquire as propriedades (métodos e campos) da outra. Pode ser *simples*, *múltipla* ou *multinível*
## Herança múltipla: classe filha herda de mais de uma classe
## Herança multinível: classe filha herda atributos de uma classe que, por sua vez, herda de outra classe

# Polimorfismo: característica de poder atribuir a um significado ou uso diferente a algo em diferentes contextos - especificamente, permitir que uma entidade como uma função ou um objeto tenha mais de uma forma

# Encapsulamento: uma barreira protetora, que impede que os dados possam ser acessados aleatoriamente por outro código definido fora da classe. O acesso aos dados e ao código é rigidamente controlado por uma classe

